class SignalType(str, Enum):
    """Trading signal direction."""
    BUY = "BUY"
    SELL = "SELL"
    HOLD = "HOLD"
    SHORT = "SHORT"      # For future margin support
    COVER = "COVER"      # For future margin support


class StrategyMode(str, Enum):
    """Strategy execution mode."""
    LIVE = "LIVE"         # Real money
    PAPER = "PAPER"       # Paper trading
    SHADOW = "SHADOW"     # Shadow testing new parameters
    BACKTEST = "BACKTEST" # Historical backtesting


class MarketRegimeAffinity(str, Enum):
    """How well a strategy performs in a given regime."""
    STRONG = "STRONG"     # Strategy excels
    NEUTRAL = "NEUTRAL"   # Strategy is unaffected
    WEAK = "WEAK"         # Strategy underperforms
    DISABLED = "DISABLED" # Strategy should not trade


