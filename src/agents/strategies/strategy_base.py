"""
Strategy Agent Base Class

Foundation for all algorithmic trading strategy agents in the SelfEvolve system.
Each strategy agent is a self-contained trading bot with:
  - Deterministic signal generation (no LLM in the hot path)
  - Self-evolving parameters (adjusted via statistical testing)
  - Full trade journaling (every decision stored with context)
  - Shadow mode (paper trade new parameters before promoting)

The strategy agent does NOT decide how much capital it gets —
that's the Portfolio Manager's job. It operates within its allocation.
"""

