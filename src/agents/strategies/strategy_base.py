class StrategyParameters(BaseModel):
    """
    Mutable parameter set for a strategy agent.
    
    These parameters evolve through statistical testing —
    a new parameter set is only promoted to production if it
    demonstrates statistically significant improvement (p < 0.05)
    over the current production parameters.
    """
    version: int = Field(default=1, ge=1, description="Parameter version number")
    params: dict[str, Any] = Field(
        default_factory=dict,
        description="Strategy-specific parameters",
    )
    promoted_at: Optional[datetime] = Field(
        default=None,
        description="When these params were promoted to production",
    )
    parent_version: Optional[int] = Field(
        default=None,
        description="Version these params evolved from",
    )
    change_description: str = Field(
        default="Initial parameters",
        description="What changed in this version and why",
    )


class StrategySignal(BaseModel):
    """
    A trading signal generated by a strategy agent.
    
    This is the primary output of every strategy agent.
    Signals flow to the Portfolio Manager for capital allocation.
    """
    signal_id: str = Field(
        default_factory=lambda: str(uuid.uuid4()),
        description="Unique signal identifier",
    )
    strategy_name: str = Field(..., description="Source strategy agent name")
    strategy_version: int = Field(default=1, description="Parameter version used")
    ticker: str = Field(..., description="Asset symbol")
    signal_type: SignalType = Field(..., description="BUY/SELL/HOLD")
    strength: float = Field(
        ..., ge=0.0, le=1.0,
        description="Signal strength (0 = weak, 1 = very strong)",
    )
    confidence: float = Field(
        ..., ge=0.0, le=1.0,
        description="Confidence in the signal (0 = uncertain, 1 = certain)",
    )
    entry_price: Optional[float] = Field(
        default=None, ge=0,
        description="Suggested entry price",
    )
    stop_loss_price: Optional[float] = Field(
        default=None, ge=0,
        description="Suggested stop loss price",
    )
    take_profit_price: Optional[float] = Field(
        default=None, ge=0,
        description="Suggested take profit price",
    )
    rationale: str = Field(
        default="",
        description="Brief explanation of why this signal was generated",
    )
    market_data_snapshot: dict = Field(
        default_factory=dict,
        description="Snapshot of market data at signal generation time (for post-mortem)",
    )
    generated_at: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )

    @field_validator("ticker")
    @classmethod
    def uppercase_ticker(cls, v: str) -> str:
        return v.upper().strip()

    @property
    def risk_reward_ratio(self) -> Optional[float]:
        """Calculate risk:reward ratio if SL and TP are set."""
        if (
            self.entry_price
            and self.stop_loss_price
            and self.take_profit_price
            and self.entry_price != self.stop_loss_price
        ):
            risk = abs(self.entry_price - self.stop_loss_price)
            reward = abs(self.take_profit_price - self.entry_price)
            return round(reward / risk, 2) if risk > 0 else None
        return None


class TradeRecord(BaseModel):
    """
    A completed trade record for the strategy journal.
    
    Every trade is stored with full context (market data at decision time)
    for post-mortem evaluation by the self-evolution engine.
    """
    trade_id: str = Field(
        default_factory=lambda: str(uuid.uuid4()),
    )
    signal_id: str = Field(..., description="Original signal that triggered this trade")
    strategy_name: str = Field(..., description="Source strategy")
    strategy_version: int = Field(default=1, description="Parameter version used")
    ticker: str = Field(..., description="Asset traded")
    action: SignalType = Field(..., description="BUY or SELL")
    entry_price: float = Field(..., ge=0, description="Actual fill price")
    exit_price: Optional[float] = Field(default=None, ge=0, description="Exit fill price")
    quantity: float = Field(..., ge=0, description="Shares (fractional)")
    notional: float = Field(..., ge=0, description="Dollar amount")
    stop_loss_price: Optional[float] = Field(default=None)
    take_profit_price: Optional[float] = Field(default=None)
    pnl_usd: Optional[float] = Field(default=None, description="Realized P&L in USD")
    pnl_pct: Optional[float] = Field(default=None, description="Realized P&L as percentage")
    is_winner: Optional[bool] = Field(default=None, description="Was this trade profitable?")
    entry_time: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )
    exit_time: Optional[datetime] = Field(default=None)
    hold_duration_minutes: Optional[int] = Field(default=None)
    market_data_at_entry: dict = Field(
        default_factory=dict,
        description="Full market data snapshot at entry (for Brier score evaluation)",
    )
    market_data_at_exit: dict = Field(default_factory=dict)
    predicted_probability: Optional[float] = Field(
        default=None, ge=0.0, le=1.0,
        description="Agent's predicted probability of profit (for Brier scoring)",
    )
    mode: StrategyMode = Field(
        default=StrategyMode.PAPER,
        description="Was this a live, paper, or shadow trade?",
    )

    def close(
        self,
        exit_price: float,
        exit_time: Optional[datetime] = None,
        market_data: Optional[dict] = None,
    ) -> None:
        """Close the trade with exit data."""
        self.exit_price = exit_price
        self.exit_time = exit_time or datetime.now(timezone.utc)
        if market_data:
            self.market_data_at_exit = market_data
        # Calculate P&L
        if self.action == SignalType.BUY:
            self.pnl_usd = round((exit_price - self.entry_price) * self.quantity, 4)
            self.pnl_pct = round(
                ((exit_price - self.entry_price) / self.entry_price) * 100, 4
            ) if self.entry_price > 0 else 0.0
        elif self.action == SignalType.SELL:
            self.pnl_usd = round((self.entry_price - exit_price) * self.quantity, 4)
            self.pnl_pct = round(
                ((self.entry_price - exit_price) / self.entry_price) * 100, 4
            ) if self.entry_price > 0 else 0.0
        self.is_winner = (self.pnl_usd or 0) > 0
        if self.exit_time and self.entry_time:
            delta = self.exit_time - self.entry_time
            self.hold_duration_minutes = int(delta.total_seconds() / 60)


