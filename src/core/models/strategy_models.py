class AllocationDecision(BaseModel):
    """
    Portfolio Manager's allocation decision across all strategies.

    Determined by performance, regime, and risk constraints.
    """
    strategy_allocations: dict[str, float] = Field(
        ..., description="strategy_name → % of portfolio (must sum to <= 100)",
    )
    cash_reserve_pct: float = Field(
        default=10.0, ge=0.0, le=100.0,
        description="Cash held in reserve",
    )
    regime: MarketRegimeType = Field(
        ..., description="Current market regime influencing allocation",
    )
    reasoning: str = Field(
        ..., description="Explanation of allocation logic",
    )
    total_portfolio_value: float = Field(
        default=0.0, description="Total portfolio value at decision time",
    )
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )

    @computed_field
    @property
    def total_allocated_pct(self) -> float:
        """Total % allocated to strategies."""
        return round(sum(self.strategy_allocations.values()), 2)

    @field_validator("strategy_allocations")
    @classmethod
    def validate_allocations(cls, v: dict[str, float]) -> dict[str, float]:
        """Ensure no single strategy gets > 40% and total <= 100%."""
        total = sum(v.values())
        if total > 100.0:
            raise ValueError(
                f"Total allocation {total}% exceeds 100%. "
                "Reduce strategy weights."
            )
        for name, pct in v.items():
            if pct < 0:
                raise ValueError(f"Strategy '{name}' has negative allocation: {pct}%")
            if pct > 40:
                raise ValueError(
                    f"Strategy '{name}' allocation {pct}% exceeds 40% max. "
                    "Diversification required."
                )
        return v


class StrategyCorrelation(BaseModel):
    """Pairwise correlation between two strategies' returns."""
    strategy_a: str
    strategy_b: str
    correlation: float = Field(
        ..., ge=-1.0, le=1.0,
        description="Pearson correlation of daily returns",
    )
    period_days: int = Field(
        default=30, description="Lookback period for calculation",
    )
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )


class PortfolioSnapshot(BaseModel):
    """
    Point-in-time snapshot of the multi-strategy portfolio.

    Used for performance attribution — "which strategy made the money?"
    """
    total_equity: float = Field(default=0.0)
    strategy_pnl: dict[str, float] = Field(
        default_factory=dict,
        description="P&L per strategy",
    )
    strategy_allocations: dict[str, float] = Field(
        default_factory=dict,
        description="Current allocation per strategy",
    )
    daily_pnl: float = Field(default=0.0)
    weekly_pnl: float = Field(default=0.0)
    monthly_pnl: float = Field(default=0.0)
    active_strategies: int = Field(default=0)
    active_positions: int = Field(default=0)
    regime: Optional[MarketRegimeType] = Field(default=None)
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )
