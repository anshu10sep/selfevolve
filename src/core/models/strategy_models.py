# STRATEGY PERFORMANCE
# ════════════════════════════════════════════════════════════════════

class StrategyPerformance(BaseModel):
    """
    Aggregated performance metrics for a single strategy.

    Calculated deterministically from TradeRecords — never by LLM.
    """
    strategy_name: str = Field(..., description="Strategy identifier")
    strategy_type: StrategyType = Field(..., description="Strategy archetype")
    status: StrategyStatus = Field(
        default=StrategyStatus.ACTIVE,
    )
    total_trades: int = Field(default=0, ge=0)
    winning_trades: int = Field(default=0, ge=0)
    losing_trades: int = Field(default=0, ge=0)
    total_pnl: float = Field(default=0.0, description="Cumulative P&L ($)")
    total_pnl_pct: float = Field(default=0.0, description="Cumulative P&L (%)")
    sharpe_ratio: float = Field(default=0.0, description="Annualized Sharpe")
    sortino_ratio: float = Field(default=0.0, description="Annualized Sortino")
    max_drawdown_pct: float = Field(default=0.0, description="Maximum drawdown (%)")
    profit_factor: float = Field(default=0.0, description="Gross profit / gross loss")
    avg_win_pct: float = Field(default=0.0, description="Average winning trade (%)")
    avg_loss_pct: float = Field(default=0.0, description="Average losing trade (%)")
    avg_hold_period_hours: float = Field(default=0.0)
    current_params_version: int = Field(default=1)
    allocation_pct: float = Field(
        default=0.0, ge=0.0, le=100.0,
        description="Current portfolio allocation (%)",
    )
    brier_score: float = Field(
        default=0.5, ge=0.0, le=1.0,
        description="Prediction calibration (lower = better)",
    )
    consecutive_losses: int = Field(default=0, ge=0)
    last_trade_time: Optional[datetime] = Field(default=None)
    updated_at: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )

    @computed_field
    @property
    def win_rate(self) -> float:
        """Win rate as fraction."""
        if self.total_trades == 0:
            return 0.0
        return round(self.winning_trades / self.total_trades, 4)

    @computed_field
    @property
    def expectancy(self) -> float:
        """Expected value per trade = (win_rate × avg_win) - (loss_rate × avg_loss)."""
        wr = self.win_rate
        lr = 1.0 - wr
        return round(wr * self.avg_win_pct - lr * abs(self.avg_loss_pct), 4)

    @computed_field
    @property
    def should_pause(self) -> bool:
        """Automatic pause trigger: 5+ consecutive losses or drawdown > 15%."""
        return self.consecutive_losses >= 5 or self.max_drawdown_pct > 15.0

    @staticmethod
    def compute_from_trades(
        strategy_name: str,
        strategy_type: StrategyType,
        trades: list[TradeRecord],
        allocation_pct: float = 0.0,
    ) -> "StrategyPerformance":
        """
        Compute performance metrics from a list of closed trades.

        All math is deterministic Python — no LLM involvement.
        """
        closed = [t for t in trades if t.exit_price is not None]
        if not closed:
            return StrategyPerformance(
                strategy_name=strategy_name,
                strategy_type=strategy_type,
                allocation_pct=allocation_pct,
            )

        pnls = [t.pnl_pct for t in closed]
        wins = [p for p in pnls if p > 0]
        losses = [p for p in pnls if p <= 0]

        total_pnl = sum(t.pnl for t in closed)
        avg_win = sum(wins) / len(wins) if wins else 0.0
        avg_loss = sum(losses) / len(losses) if losses else 0.0

        # Sharpe ratio (annualized, assuming ~252 trading days)
        if len(pnls) > 1:
            mean_ret = sum(pnls) / len(pnls)
            std_ret = math.sqrt(
                sum((p - mean_ret) ** 2 for p in pnls) / (len(pnls) - 1)
            )
            sharpe = (mean_ret / std_ret * math.sqrt(252)) if std_ret > 0 else 0.0

            # Sortino (only downside deviation)
            downside = [p for p in pnls if p < 0]
            if downside:
                down_std = math.sqrt(sum(p ** 2 for p in downside) / len(downside))
                sortino = (mean_ret / down_std * math.sqrt(252)) if down_std > 0 else 0.0
            else:
                sortino = sharpe * 1.5  # All wins = great sortino
        else:
            sharpe = 0.0
            sortino = 0.0

        # Max drawdown
        cumulative = 0.0
        peak = 0.0
        max_dd = 0.0
        for p in pnls:
            cumulative += p
            peak = max(peak, cumulative)
            dd = peak - cumulative
            max_dd = max(max_dd, dd)

        # Profit factor
        gross_profit = sum(wins) if wins else 0.0
        gross_loss = abs(sum(losses)) if losses else 1.0  # avoid /0
        profit_factor = gross_profit / gross_loss if gross_loss > 0 else gross_profit

        # Consecutive losses (from end)
        consec = 0
        for p in reversed(pnls):
            if p <= 0:
                consec += 1
            else:
                break

        # Average hold period
        hold_hours = [t.hold_duration_hours for t in closed if t.hold_duration_hours > 0]
        avg_hold = sum(hold_hours) / len(hold_hours) if hold_hours else 0.0

        return StrategyPerformance(
            strategy_name=strategy_name,
            strategy_type=strategy_type,
            total_trades=len(closed),
            winning_trades=len(wins),
            losing_trades=len(losses),
            total_pnl=round(total_pnl, 2),
            total_pnl_pct=round(sum(pnls), 2),
            sharpe_ratio=round(sharpe, 2),
            sortino_ratio=round(sortino, 2),
            max_drawdown_pct=round(max_dd, 2),
            profit_factor=round(profit_factor, 2),
            avg_win_pct=round(avg_win, 2),
            avg_loss_pct=round(avg_loss, 2),
            avg_hold_period_hours=round(avg_hold, 2),
            allocation_pct=allocation_pct,
            consecutive_losses=consec,
            last_trade_time=closed[-1].exit_time if closed else None,
        )


