class Signal(BaseModel):
    """
    Trading signal generated by a strategy agent.

    This is the atomic unit of strategy output — a typed, validated
    instruction that flows to the Portfolio Manager for aggregation.
    """
    signal_id: str = Field(
        default_factory=lambda: str(uuid.uuid4()),
        description="Unique signal identifier",
    )
    ticker: str = Field(..., description="Asset symbol")
    strategy_name: str = Field(..., description="Source strategy agent name")
    strategy_type: StrategyType = Field(..., description="Strategy archetype")
    direction: SignalDirection = Field(..., description="Trade direction")
    strength: float = Field(
        ..., ge=0.0, le=1.0,
        description="Signal strength (0 = weak, 1 = maximum conviction)",
    )
    entry_price: float = Field(..., gt=0, description="Recommended entry price")
    stop_loss: float = Field(..., gt=0, description="Stop loss price")
    take_profit: float = Field(..., gt=0, description="Take profit target")
    confidence: float = Field(
        ..., ge=0.0, le=1.0,
        description="Strategy confidence in this signal",
    )
    reasoning: str = Field(
        ..., max_length=500,
        description="Deterministic reasoning for the signal",
    )
    parameter_version: int = Field(
        default=1,
        description="Parameter version that generated this signal",
    )
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )

    @field_validator("ticker")
    @classmethod
    def uppercase_ticker(cls, v: str) -> str:
        return v.upper().strip()

    @computed_field
    @property
    def risk_reward_ratio(self) -> float:
        """Risk/reward ratio. Higher is better (target > 2.0)."""
        if self.direction == SignalDirection.LONG:
            risk = self.entry_price - self.stop_loss
            reward = self.take_profit - self.entry_price
        elif self.direction == SignalDirection.SHORT:
            risk = self.stop_loss - self.entry_price
            reward = self.entry_price - self.take_profit
        else:
            return 0.0
        if risk <= 0:
            return 0.0
        return round(reward / risk, 2)

    @computed_field
    @property
    def risk_pct(self) -> float:
        """Percentage risk from entry to stop loss."""
        if self.entry_price == 0:
            return 0.0
        if self.direction == SignalDirection.LONG:
            return round(abs(self.entry_price - self.stop_loss) / self.entry_price * 100, 2)
        elif self.direction == SignalDirection.SHORT:
            return round(abs(self.stop_loss - self.entry_price) / self.entry_price * 100, 2)
        return 0.0


class EntryDecision(BaseModel):
    """Strategy agent's entry decision for a specific ticker."""
    should_enter: bool = Field(..., description="Whether to enter the position")
    signal: Optional[Signal] = Field(
        default=None, description="Signal details if entering",
    )
    reasoning: str = Field(..., description="Explanation for the decision")
    data_snapshot: dict = Field(
        default_factory=dict,
        description="Market data available at decision time (for post-mortem)",
    )


class ExitDecision(BaseModel):
    """Strategy agent's exit decision for an open position."""
    should_exit: bool = Field(..., description="Whether to exit the position")
    exit_type: Optional[ExitType] = Field(
        default=None, description="Reason for exit",
    )
    exit_price: Optional[float] = Field(
        default=None, description="Recommended exit price",
    )
    reasoning: str = Field(..., description="Explanation for the decision")


