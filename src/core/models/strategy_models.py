class TradeRecord(BaseModel):
    """
    Complete record of a strategy trade for performance attribution.

    Captures everything needed for post-mortem analysis and learning.
    """
    trade_id: str = Field(
        default_factory=lambda: str(uuid.uuid4()),
    )
    strategy_name: str = Field(..., description="Strategy that generated the trade")
    strategy_type: StrategyType = Field(..., description="Strategy archetype")
    ticker: str = Field(..., description="Asset traded")
    direction: SignalDirection = Field(..., description="Trade direction")
    entry_price: float = Field(..., gt=0, description="Actual entry price")
    exit_price: Optional[float] = Field(default=None, description="Actual exit price")
    quantity: float = Field(..., gt=0, description="Shares traded")
    notional: float = Field(..., gt=0, description="Dollar amount")
    stop_loss: float = Field(..., gt=0, description="Stop loss at entry")
    take_profit: float = Field(..., gt=0, description="Take profit at entry")
    entry_time: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )
    exit_time: Optional[datetime] = Field(default=None)
    exit_type: Optional[ExitType] = Field(default=None)
    signal_strength: float = Field(default=0.0, ge=0.0, le=1.0)
    signal_confidence: float = Field(default=0.0, ge=0.0, le=1.0)
    parameter_version: int = Field(default=1)
    market_regime: Optional[MarketRegimeType] = Field(default=None)
    data_at_entry: dict = Field(
        default_factory=dict,
        description="Market data snapshot at entry for post-mortem replay",
    )
    data_at_exit: dict = Field(
        default_factory=dict,
        description="Market data snapshot at exit",
    )

    @field_validator("ticker")
    @classmethod
    def uppercase_ticker(cls, v: str) -> str:
        return v.upper().strip()

    @computed_field
    @property
    def pnl(self) -> float:
        """Realized P&L for closed trades."""
        if self.exit_price is None:
            return 0.0
        if self.direction == SignalDirection.LONG:
            return (self.exit_price - self.entry_price) * self.quantity
        elif self.direction == SignalDirection.SHORT:
            return (self.entry_price - self.exit_price) * self.quantity
        return 0.0

    @computed_field
    @property
    def pnl_pct(self) -> float:
        """Realized P&L as percentage."""
        if self.exit_price is None or self.entry_price == 0:
            return 0.0
        if self.direction == SignalDirection.LONG:
            return round((self.exit_price - self.entry_price) / self.entry_price * 100, 2)
        elif self.direction == SignalDirection.SHORT:
            return round((self.entry_price - self.exit_price) / self.entry_price * 100, 2)
        return 0.0

    @computed_field
    @property
    def is_winner(self) -> bool:
        """Whether this trade was profitable."""
        return self.pnl > 0

    @computed_field
    @property
    def hold_duration_hours(self) -> float:
        """How long the position was held."""
        if self.exit_time is None:
            return 0.0
        delta = self.exit_time - self.entry_time
        return round(delta.total_seconds() / 3600, 2)


