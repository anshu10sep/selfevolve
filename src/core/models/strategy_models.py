class ParameterVersion(BaseModel):
    """
    A versioned snapshot of strategy parameters.

    Every parameter change is logged with full context for
    audit trail and rollback capability.
    """
    version: int = Field(..., ge=1, description="Sequential version number")
    params: dict = Field(..., description="The parameter values")
    activated_at: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )
    deactivated_at: Optional[datetime] = Field(
        default=None, description="When this version was replaced",
    )
    trade_count: int = Field(default=0, ge=0, description="Trades under this version")
    win_count: int = Field(default=0, ge=0, description="Winning trades")
    total_pnl: float = Field(default=0.0, description="Cumulative P&L")
    sharpe: float = Field(default=0.0, description="Sharpe ratio under this version")
    max_drawdown: float = Field(default=0.0, description="Max drawdown under this version")
    status: ParameterStatus = Field(
        default=ParameterStatus.LIVE,
        description="Lifecycle state",
    )
    change_reason: str = Field(
        default="", description="Why this version was created",
    )

    @computed_field
    @property
    def win_rate(self) -> float:
        """Win rate under this parameter version."""
        if self.trade_count == 0:
            return 0.0
        return round(self.win_count / self.trade_count, 4)

    @computed_field
    @property
    def avg_pnl_per_trade(self) -> float:
        """Average P&L per trade."""
        if self.trade_count == 0:
            return 0.0
        return round(self.total_pnl / self.trade_count, 4)


class ParameterEvolution(BaseModel):
    """
    A proposed parameter change with statistical justification.

    Only promoted to production when p < 0.05 vs current params.
    """
    strategy_name: str = Field(..., description="Source strategy")
    current_version: int = Field(..., description="Current parameter version")
    proposed_version: int = Field(..., description="Proposed version number")
    current_params: dict = Field(..., description="Current parameter values")
    proposed_params: dict = Field(..., description="Proposed parameter values")
    changes: dict = Field(
        ..., description="Params that changed: {name: {old, new, reason}}",
    )
    statistical_evidence: dict = Field(
        default_factory=dict,
        description="T-test results, p-value, confidence interval",
    )
    backtest_comparison: dict = Field(
        default_factory=dict,
        description="Current vs proposed backtest results",
    )
    recommendation: Literal["PROMOTE", "CONTINUE_TESTING", "REJECT"] = Field(
        default="CONTINUE_TESTING",
    )
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
    )


