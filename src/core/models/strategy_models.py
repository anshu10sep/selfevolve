"""
Strategy Domain Models

Pydantic models for multi-strategy trading signals, parameter versioning,
performance attribution, and portfolio allocation decisions.

These models enforce type safety across the entire strategy pipeline:
    Strategy Agent → Signal → Portfolio Manager → Allocation → Execution
"""

from __future__ import annotations

import uuid
import math
from datetime import datetime, timezone
from enum import Enum
from typing import Literal, Optional

from pydantic import BaseModel, Field, field_validator, computed_field


# ════════════════════════════════════════════════════════════════════
# ENUMS
